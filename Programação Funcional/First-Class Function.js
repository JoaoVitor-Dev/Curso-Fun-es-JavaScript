

const add = function(a, b) {  //function expression
    return a + b
}
const subtrack = function(a, b) {  //function expression
    return a - b
}

const multiply = (x, y) => x * y // arrow function

const divide = (x, y) => x / y  // arrow function


console.log(add(10, 20));
console.log(subtrack(10, 20));
console.log(multiply(10, 20));
console.log(divide(10, 20));

